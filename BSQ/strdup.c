#include "bsq.h"

char	*ft_strcpy(char *dest, char *src)
{
	int i;

	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = src[i];
	return (dest);
}

char	*ft_strdup(char *src)
{
	char *temp;

	temp = (char *)malloc(ft_strlen(src) + 1);
	ft_strcpy(temp, src);
	return (temp);
}
