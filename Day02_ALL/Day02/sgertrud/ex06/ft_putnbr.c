void	ft_putnbr(int nb)
		{
			char digits[10];
			int i;

			i = 0;
			while (i < 10)
			{
				digits[9 - i] = '0' + nb % 10;
				nb /= 10;
				i++;
			}
			i = 0;
			while (digits[i] == '0')
			{
				i++;
			}
			while (i < 10)
			{
				ft_putchar(digits[i]);
				i++;
			}
		}
