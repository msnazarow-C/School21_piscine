int		ft_is_prime(int nb)
{
	int q;

	if (nb < 2)
	{
		return (0);
	}
	if (nb == 2)
	{
		return (1);
	}
	q = 2;
	while (nb % q != 0)
	{
		q++;
		if (q > nb / q)
		{
			return (1);
		}
	}
	return (0);
}
