int		alpha(char c)
{
	return ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'));
}

int 	ft_str_is_alpha(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		if  (!alpha(str[i]))
			return (0);
		i++;
	}
	return (1);
}
