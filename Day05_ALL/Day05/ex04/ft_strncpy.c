char    *ft_strncpy(char *dst, char *src, unsigned int n)
{
	int i;

	i = 0;
	while (i < n)
	{
		dst[i] = src[i];
		i++;
	}
	while ( src[i] != 0 )
	{
		dst[i] = '\0';
		i++;
	}
	dst[i] = '\0';
	return dst;
}
