char *ft_strcapitalize(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] <= '9' && str[i] >= '0')
		{
			i++;
			while ((str[i] >= 'a' && str[i] <='z') || (str[i] <= '9' && str[i] >= '0'))
			i++;
		}
		if (str[i] >= 'a' && str[i] <= 'z')
		{
			str[i] = str[i] + 'A' - 'a';
		i++;
		}
			while ((str[i] >= 'a' && str[i] <='z') || (str[i] <= '9' && str[i] >= '0'))
			i++;
		i++;
	}
		return str;
}
