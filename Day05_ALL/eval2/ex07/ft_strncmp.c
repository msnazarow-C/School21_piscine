int		ft_strncmp(char *str1, char *str2, unsigned int n)
{
	unsigned int i;

	i = 0;
	while (str1[i] == str2[i] && i < n - 1)
		i++;
	if (n == 0)
		return (0);
	return (str1[i] - str2[i]);
}
