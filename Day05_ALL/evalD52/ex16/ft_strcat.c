char	*ft_strcat(char *dest, char *src)
{
	int i;
	int j;

	j = 0;
	i = 0;
	while (dest[i])
	{
		i++;
	}
	while (dest[i + j])
	{
		dest[i + j] = src[j];
		j++;
	}
	return (dest);
}
