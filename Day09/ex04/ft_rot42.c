char	*ft_rot42(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		if ((str[i] >= 65 && str[i] <= 90) || (str[i] >= 97 && str[i] <= 122))
		{
			if ((str[i] >= 65 && str[i] < 75) || (str[i] >= 97 && str[i] < 107))
			{
				str[i] = str[i] + 16;
			}
			else
			{
				str[i] = str[i] - 10;
			}
		}
		i++;
	}
	return (str);
}
