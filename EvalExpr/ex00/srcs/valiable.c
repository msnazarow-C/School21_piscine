#include "ft.h"

int		isd(char c)
{
	return (c >= '0' && c <= '9');
}

int		isop(char c)
{
	return ((c >= 40 && c <= 43) || c == 45 || c == 47 || c == 37);
}

int		valiable(char c)
{
	return (isop(c) || isd(c));
}
